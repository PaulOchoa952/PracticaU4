    private void btnApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyActionPerformed
         int opcion = cmbSelecc.getSelectedIndex();
         char f=' ', 
         destiny = ' ';
         
        if(opcion<5){
        if(txtOrigen.getText().length()==0){ this.setTitle("¡Ingresa un dato!"); return; }
            f = txtOrigen.getText().toUpperCase().charAt(0);
        }
        if(opcion>2 && opcion<5)  destiny = (char) cmbDestiny.getSelectedItem();
        boolean respuesta;
        
        switch (opcion){
            case 1:
                respuesta = grafito.insertarVertice(f);
                mostrarRespuesta(respuesta); 
                vert+=f+", "; 
                if(respuesta){ cmbVertices.addItem(f);
                cmbDestiny.addItem(f);
                }
            break;
            
            case 2: 
                respuesta = grafito.eliminarVertice(f); 
                mostrarRespuesta(respuesta);  
                if(respuesta) cmbVertices.removeItem(f);
            break;
            
            case 3: 
                respuesta = grafito.insertarArista(f, destiny);  
                mostrarRespuesta(respuesta);
                if(respuesta) ari+=f+","+destiny+" ";
            break;
            
            case 4: 
                respuesta = grafito.eliminarArista(f, destiny);
                mostrarRespuesta(respuesta);
                String aristaEliminada = f+","+destiny; 
                mostrarAristas(respuesta, aristaEliminada);
                
            break;
            
            case 5:
                if(ari.length()==0){ txtmTriz.setText("No tienes aristas para ejecutar Matriz "); return;}
                String help1 = vert.replaceAll(", ", ""); 
                
                
                
                char [] help = help1.toCharArray();
                boolean datos[][] = grafito.matrizAdyacencia();
                String cad = "";
                for (int i = 0; i < help.length; i++) {
                    cad += help[i];
                }

                for (int i = 0; i < help.length; i++) {
                    cad += "\n" + help[i] + " ";
                    for (int j = 0; j < help.length; j++) {
                        if(datos[i][j]){
                            cad+="1 ";
                        }else{
                            cad+="0 ";
                        }
                    }
                }
                txtmTriz.setText(cad);
            break;
            
            case 6: 
                if (cmbVertices.getItemCount() > 0)//Verifica cuanto elementos tiene la lista
                txtmTriz.setText(grafito.listaAdyacencia((char) cmbVertices.getSelectedItem()));
            break;    
        }
        
        if(cmbSelecc.getSelectedIndex()!=4){ 
            txtaRista.setText(ari); 
        }
         
    }//GEN-LAST:event_btnApplyActionPerformed

