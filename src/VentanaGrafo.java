    public void mostrarAristas(boolean respuesta, String aristaEliminada){
        String[] arisTas = ari.split(" ");
            if(respuesta){
                ari ="";
                String cad="";
                for(int i=0; i<arisTas.length; i++){
                    if(arisTas[i].equals(aristaEliminada)){
                        cad+= "";
                        ari += "";
                    }else{
                        cad = cad+arisTas[i]+" ";
                        ari += arisTas[i]+" ";
                        }
                    }
                    txtaRista.setText(cad);
            }
    }
    
    public void mostrarRespuesta(boolean respuesta){
        String mensaje = "Inserción/Eliminación erronea";
        if(respuesta) mensaje = "Operacion Realizada";
        setTitle(mensaje);
        txtOrigen.setText("");
        
    }
    
